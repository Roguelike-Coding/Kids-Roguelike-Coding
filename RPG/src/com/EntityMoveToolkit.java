package com;

import javax.swing.JLayeredPane;

public class EntityMoveToolkit {
	public static EntityLabel moveUp(EntityLabel entity, JLayeredPane pane){
		entity.setLocation(entity.getX(), entity.getY()-1);
		//pane.setLayer(entity, 0, arg2);
		return entity;
		
	}
	public static EntityLabel moveDown(EntityLabel entity, JLayeredPane pane){
		entity.setLocation(entity.getX(), entity.getY()+1);
		return entity;
		
	}
	public static EntityLabel moveLeft(EntityLabel entity, JLayeredPane pane){
		entity.setLocation(entity.getX()-2, entity.getY());
		return entity;
	
	}
	public static EntityLabel moveRight(EntityLabel entity, JLayeredPane pane){
		entity.setLocation(entity.getX()+2, entity.getY());
		return entity;
	
	}
}
